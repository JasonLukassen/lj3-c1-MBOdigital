2. Uitwerking
D1-K1: Oriënteert zich op het ondernemerschap
Complexiteit
De beroepsbeoefenaar onderzoekt of het ondernemerschap iets voor hem is door verschillende werkwijzen te hanteren en te
combineren in wisselende situaties.
Voor de oriëntatie op ondernemerschap is (basis)kennis noodzakelijk ten aanzien van zelfstandig ondernemerschap,
marktonderzoek, marketing en promotie en financiële administratie. Verder is hij in staat om technieken op het gebied van
communicatie en zelfreflectie toe te passen.
Verantwoordelijkheid en zelfstandigheid
De beroepsbeoefenaar draagt verantwoordelijkheid voor de resultaten van eigen activiteiten. Bij de oriëntatie op het
ondernemerschap wordt hij begeleid en werkt hij mogelijk samen.
Vakkennis en vaardigheden
De beginnend beroepsbeoefenaar:
§ Bezit basiskennis van juridische ondernemingsvormen
§ Bezit basiskennis van voor de onderneming/sector relevante (commerciële) samenwerkingsvormen
§ Bezit basiskennis van interne en externe financieringsvormen
§ Bezit kennis van SWOT-analyse
§ Bezit basiskennis van doelgroepanalyse
§ Bezit basiskennis van onderzoek en onderzoeksmethoden
§ Bezit basiskennis van gehanteerde marketingstrategieën in het vakgebied
§ Bezit basiskennis van voor de onderneming/sector benodigde verzekeringen, vergunningen e.d.
§ Bezit kennis om sociale media, e-commerce, webcare, etc. voor commerciële doeleinden in te zetten
§ Bezit basiskennis over relevante wet- en regelgeving voor het starten van een onderneming
§ Kan eenvoudige commercieel-economische gegevems t.b.v. een onderneming interpreteren
§ Bezit basiskennis over relevante wet- en regelgeving voor het runnen van een onderneming
§ Kan gesprekstechnieken in zakelijke situaties toepassen
§ Kan ideeën mondeling of schriftelijk presenteren
§ Kan reflectievaardigheden toepassen
§ Bezit basiskennis over duurzaam en maatschappelijk verantwoord ondernemerschap
D1-K1-W1: Oriënteert zich op het starten van een onderneming
Omschrijving
De beroepsbeoefenaar onderzoekt met behulp van verschillende informatiebronnen wat het betekent om een onderneming te
starten. Hij bedenkt waarin hij zou kunnen of willen ondernemen. Hij gaat na of hij anderen met zijn ideeën kan enthousiasmeren
en of anderen openstaan voor zijn ideeën. Hij onderzoekt wat goed is aan zijn idee, wat zwakke punten zijn van zijn idee en hoe
hij hiermee kan omgaan.
Hij gaat na hoe hij zijn ideeën kan vermarkten: wat is zijn aanbod, wat wil hij daarvoor krijgen en wat levert het hem op. Hij denkt
na over zijn aanbod, de presentatie van zijn aanbod en wat hij nodig heeft om zijn plannen te realiseren. Ook gaat hij na hoeveel
geld hij nodig heeft en welke mogelijkheden er zijn om aan geld te komen. Hij weegt verschillende mogelijkheden af en
onderzoekt wat er in een plan voor een onderneming moet komen te staan. Hij presenteert zijn idee (mondeling of schriftelijk)
aan anderen, verzamelt feedback op zijn idee en schat de slagingskans van het idee in.
Resultaat
Inzicht in en/of ervaring met wat er komt kijken bij het starten van een onderneming: idee vormen, financiering vinden,
slagingskans inschatten en strategie om de markt te benaderen.
Gedrag
- Onderzoekt kritisch welke informatie je nodig hebt om te bepalen wat er komt kijken bij het opstarten van een onderneming,
waar je die informatie kunt vinden en wat de waarde van de betreffende bronnen is.
D1-K1-W1: Oriënteert zich op het starten van een onderneming
- Gebruikt verschillende invalshoeken om te kijken naar de kansen van een onderneming en om alternatieven te vinden voor
(mogelijke) zwakke punten en bedreigingen.
- Toont realiteitszin en inzicht in de markt bij het kiezen en beoordelen van keuzes ten aanzien van het ondernemerschap en de
slagingskans van het zelfstandig ondernemerschap.
- Toont zich vindingrijk in het zoeken naar kansen om doelgroep en aanbod bij elkaar te brengen, waarbij rekening wordt
gehouden met de ontwikkelingen in de markt, de doelgroep, etc.
De onderliggende competenties zijn: Creëren en innoveren, Ondernemend en commercieel handelen, Onderzoeken
D1-K1-W2: Oriënteert zich op het runnen van een onderneming
Omschrijving
De beroepsbeoefenaar bedenkt welke activiteiten je kunt ondernemen om klanten te krijgen, te houden en van dienst te zijn.
Waar mogelijk probeert hij verschillende activiteiten uit en bepaalt het effect ervan.
Hij gaat na welke praktische zaken hij dient te regelen voor het runnen van de onderneming: kan hij alles alleen of dient hij
partners te zoeken, aan welke eisen moeten processen en producten voldoen, welke (financiële) gegevens dient hij te verzamelen
en te bewaken, etc. Hij bespreekt zijn ideeën hierover met betrokkenen en/of probeert zaken uit, kijkt mee met een ondernemer,
oefent zaken, etc.
Resultaat
Inzicht in en/of ervaring met basisbeginselen van bedrijfsvoering: waar moet iemand rekening mee houden bij het aansturen van
een onderneming (financiële gegevens, kansen, bedreigingen e.d.), waar moet iemand prioriteit aan geven en waar moet iemand
beslissingen over nemen.
Gedrag
- Stelt zich onderzoekend en lerend op om inzicht in of ervaring met de taken van een ondernemer te krijgen.
- Combineert creativiteit met realiteitszin bij het bedenken van mogelijkheden om klanten te trekken, te boeien en te binden.
- Laat met zijn ideeën en/of handelswijze zien over een ondernemende houding en commercieel en bedrijfsmatig (financieel en
administratief) inzicht te beschikken (of toont zelfinzicht door aan te geven wat hij nog verder dient te ontwikkelen voor het
ondernemerschap).
De onderliggende competenties zijn: Creëren en innoveren, Op de behoeften en verwachtingen van de "klant"
richten, Ondernemend en commercieel handelen, Bedrijfsmatig handelen, Onderzoeken
D1-K1-W3: Besluit of ondernemen (als toekomstperspectief) bij hem past
Omschrijving
De beroepsbeoefenaar bepaalt wat hij leuk en minder leuk vindt aan ondernemen en gaat na of het starten en runnen van een
eigen onderneming bij hem past op korte of lange termijn. Hij bekijkt welke persoonskenmerken van pas komen en welke
persoonskenmerken hij nog verder zou moeten ontwikkelen. Ook gaat hij na wat het ondernemerschap verder vraagt qua inzet
(bijv. beschikbaarheid, werkomstandigheden), houding (doelgericht, gedisciplineerd, pro-actief) en vaardigheden (bijv. plannen,
organiseren, netwerken), welke mogelijkheden ondernemerschap hem kan bieden en in hoeverre dit bij hem past. Hij benut
mensen in zijn omgeving door hen feedback en advies te vragen. Op basis van zijn reflectie weet hij wat ondernemen van hem
vraagt en of hij gemotiveerd is hieraan (nu of in de toekomst) tijd, energie en middelen te besteden.
Resultaat
De beroepsbeoefenaar weet in hoeverre ondernemen bij hem past (of in de toekomst bij hem zal kunnen passen) en kent zijn/haar
kwaliteiten en ontwikkelpunten op dit gebied.
Gedrag
- Vraagt actief feedback over zijn expertise als potentiële ondernemer en staat open voor feedback.
- Bepaalt zelfstandig, met behulp van zelfkennis en feedback van anderen, zijn plus- en minpunten af als potentiële ondernemer.
- Neemt weloverwogen een beslissing of hij op korte of lange termijn zelf wil gaan ondernemen en is zich bewust van eigen groei
