    <section class="mb-8">
        <h2 class="text-2xl font-bold text-gray-800">Uitwerking</h2>
        <p class="mt-2 text-gray-700"><b>D1-K1:</b> Maakt een eenvoudige game</p>
        <p class="mt-2 text-gray-700"><b>Complexiteit:</b> De beginnend beroepsbeoefenaar heeft specialistische kennis van gameontwikkelomgevingen en het ontwikkelproces, evenals technische en creatieve vaardigheden in het programmeren van games. De werkzaamheden zijn niet routinematig, en iedere opdracht is uniek.</p>
        <p class="mt-2 text-gray-700"><b>Verantwoordelijkheid en zelfstandigheid:</b> Werkt binnen een multidisciplinair projectteam en is verantwoordelijk voor de technische realisatie van de game.</p>
        <p class="mt-2 text-gray-700"><b>Vakkennis en vaardigheden:</b></p>
        <ul class="list-disc pl-6 text-gray-700">
            <li>Specialistische kennis van gamesgerichte ontwikkelomgevingen.</li>
            <li>Specialistische kennis van de werkzaamheden van overige projectleden.</li>
            <li>Kennis van de kenmerken en functies van onderdelen van een game design document.</li>
            <li>Kennis van verschillende game engines en programmeertalen.</li>
            <li>Toepassing van game specifieke syntax.</li>
            <li>Commerciële doelen toepassen tijdens de realisatie van de game.</li>
        </ul>
    </section>

    <section class="mb-8">
        <h2 class="text-2xl font-bold text-gray-800">D1-K1-W1: Bereidt de technische realisatie van een eenvoudige game voor</h2>
        <p class="mt-2 text-gray-700"><b>Omschrijving:</b> De beginnend beroepsbeoefenaar levert een technische bijdrage aan het Game Design Document (GDD) en adviseert over de technische ontwikkeling van de game. Hij plant de werkzaamheden, richt de ontwikkelomgeving in, en zorgt voor voortgangsgesprekken.</p>
        <p class="mt-2 text-gray-700"><b>Resultaat:</b> Een correct Game Design Document en een gereedstaande game ontwikkelomgeving.</p>
        <p class="mt-2 text-gray-700"><b>Gedrag:</b></p>
        <ul class="list-disc pl-6 text-gray-700">
            <li>Komt op basis van vakkundig inzicht tot ideeën en adviezen.</li>
            <li>Kiest een geschikte IDE en programmeertaal.</li>
            <li>Bepaalt deeltaken en schat de benodigde tijd in.</li>
            <li>Stelt een duidelijke planning op.</li>
        </ul>
    </section>

    <section class="mb-8">
        <h2 class="text-2xl font-bold text-gray-800">D1-K1-W2: Realiseert en test (onderdelen van) een eenvoudige game</h2>
        <p class="mt-2 text-gray-700"><b>Omschrijving:</b> De beginnend beroepsbeoefenaar schrijft programmacode, voegt assests toe (landschappen, licht en geluid), en test de game. Hij optimaliseert vervolgens de programmacode en assets op basis van de testresultaten.</p>
        <p class="mt-2 text-gray-700"><b>Resultaat:</b> Een optimaal werkende eenvoudige game.</p>
        <p class="mt-2 text-gray-700"><b>Gedrag:</b></p>
        <ul class="list-disc pl-6 text-gray-700">
            <li>Plant nauwkeurig zijn werkzaamheden.</li>
            <li>Schrijft logische en systematische scripts.</li>
            <li>Presteert effectief onder druk.</li>
            <li>Integreert de game-assets op een vakkundige wijze die aansluiten bij de sfeer van de game.</li>
        </ul>
    </section>

    <section class="mb-8">
        <h2 class="text-2xl font-bold text-gray-800">D1-K1-W3: Levert de gerealiseerde technische onderdelen van de eenvoudige game op</h2>
        <p class="mt-2 text-gray-700"><b>Omschrijving:</b> De beginnend beroepsbeoefenaar levert de gerealiseerde game of game-onderdelen op, demonstreert het resultaat aan de opdrachtgever, en documenteert zijn activiteiten. Hij integreert de producten eventueel met die van andere teamleden en verbetert waar nodig.</p>
        <p class="mt-2 text-gray-700"><b>Resultaat:</b> Een opgeleverde eenvoudige game of game-onderdelen en bijgewerkte documentatie.</p>
        <p class="mt-2 text-gray-700"><b>Gedrag:</b></p>
        <ul class="list-disc pl-6 text-gray-700">
            <li>Presenteert de game op een overtuigende manier.</li>
            <li>Draagt de game deskundig over voor verdere ontwikkeling.</li>
            <li>Bewaakt de planning en levert producten op tijd.</li>
            <li>Documenteert de werkzaamheden nauwkeurig en voert versiebeheer.</li>
        </ul>
    </section>
